def sum(x,y):
    return x+y

def area_of_circle(r):
    return 3.14*r*r

def average(x,y,z):
    return (x+y+z)/3

def compound_Interest(p,r,t):
    return p*((1+(r/100))**t)

def cubiod_volume(l,b,h):
    return l*b*h

print(sum(3,4))
print(area_of_circle(3))
print(average(3,4,5))
print(compound_Interest(1000,10,10))
print(cubiod_volume(3,4,5))
